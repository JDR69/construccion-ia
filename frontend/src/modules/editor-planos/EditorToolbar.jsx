export function EditorToolbar({
  onBack,
  onClear,
  onSave,
  onExportJpg,
  onExportPdf,
  onUndo,
  onRedo,
  onZoomIn,
  onZoomOut,
  onZoomFit,
  saving,
  isDark,
  onToggleTheme,
  canUndo,
  canRedo,
  zoomLevel,
}) {
  const iconBtn = 'h-14 w-14 px-0 justify-center rounded-xl'
  const iconSize = 'w-5 h-5 shrink-0'
  const groupWrap =
    'flex items-center gap-2 rounded-2xl border border-slate-200 dark:border-slate-700 bg-white/95 dark:bg-slate-950/95 px-4 py-2 shadow-lg backdrop-blur'
  const rowScroll = 'flex flex-wrap items-center justify-center gap-1'

  return (
    <div className="w-full flex justify-center pb-4">
      <div className={groupWrap}>
        <div className={rowScroll}>
          {/* ── Volver ── */}
          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onBack}
            disabled={saving}
            title="Volver al dashboard"
          >
            <IconBack className={iconSize} />
          </Button>

          <div className="mx-2 h-8 w-px bg-slate-200 dark:bg-slate-700" />

          {/* ── Tema ── */}
          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onToggleTheme}
            title={isDark ? 'Modo claro' : 'Modo oscuro'}
          >
            {isDark ? <IconSun className={iconSize} /> : <IconMoon className={iconSize} />}
          </Button>

          <div className="mx-2 h-8 w-px bg-slate-200 dark:bg-slate-700" />

          {/* ── Zoom ── */}
          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onZoomOut}
            title="Zoom -"
          >
            <IconZoomOut className={iconSize} />
          </Button>

          <span className="px-2 text-sm font-medium text-slate-600 dark:text-slate-300 min-w-[50px] text-center">
            {zoomLevel || '100%'}
          </span>

          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onZoomIn}
            title="Zoom +"
          >
            <IconZoomIn className={iconSize} />
          </Button>

          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onZoomFit}
            title="Ajustar a ventana"
          >
            <IconFit className={iconSize} />
          </Button>

          <div className="mx-2 h-8 w-px bg-slate-200 dark:bg-slate-700" />

          {/* ── Undo / Redo ── */}
          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onUndo}
            disabled={!canUndo}
            title="Deshacer (Ctrl+Z)"
          >
            <IconUndo className={iconSize} />
          </Button>

          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onRedo}
            disabled={!canRedo}
            title="Rehacer (Ctrl+Shift+Z)"
          >
            <IconRedo className={iconSize} />
          </Button>

          <div className="mx-2 h-8 w-px bg-slate-200 dark:bg-slate-700" />

          {/* ── Guardar ── */}
          <Button
            variant="primary"
            size="lg"
            className={iconBtn}
            onClick={onSave}
            disabled={saving}
            title="Guardar plano"
          >
            <IconSave className={iconSize} />
          </Button>

          {/* ── Exportar ── */}
          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onExportJpg}
            title="Exportar JPG"
          >
            <IconJpg className={iconSize} />
          </Button>

          <Button
            variant="secondary"
            size="lg"
            className={iconBtn}
            onClick={onExportPdf}
            title="Exportar PDF"
          >
            <IconPdf className={iconSize} />
          </Button>

          <div className="mx-2 h-8 w-px bg-slate-200 dark:bg-slate-700" />

          {/* ── Limpiar ── */}
          <Button
            variant="danger"
            size="lg"
            className={iconBtn}
            onClick={onClear}
            disabled={saving}
            title="Limpiar plano"
          >
            <IconTrash className={iconSize} />
          </Button>
        </div>
      </div>
    </div>
  )
}
